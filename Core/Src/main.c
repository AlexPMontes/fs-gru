	    	adc_2 = adc_values[1];

	    	TxData_adc[0]=(diff >> 8) & 0xFF;
	    	TxData_adc[1]=diff & 0xFF;

	    	TxData_adc[2]=(adc_1 >> 8) & 0xFF;
